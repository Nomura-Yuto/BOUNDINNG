#include "main.h"
#include "controller.h"
#include "sound.h"
#include "title.h"
#include "titlebg.h"
#include "fade.h"


//=================================================================
// グローバル変数
//=================================================================


//=================================================================
// 初期化処理
//=================================================================
void InitTitle()
{
	InitTitleBG();

	// BGMの再生
	PlayBGM(STRM_LOVELYFLOWER);
}


//=================================================================
// 終了処理
//=================================================================
void UninitTitle()
{
	UninitTitleBG();
}


//=================================================================
// 更新処理
//=================================================================
void UpdateTitle()
{
	UpdateTitleBG();

	// シーンの切り替え
	if (GetControllerTrigger(NpadButton::A::Index)) 
	{
		// SEの再生
		PlaySE(WSD_SE_DECISION);
		SwapFade(FADE_OUT);
	}
}


//=================================================================
// 描画処理
//=================================================================
void DrawTitle()
{
	DrawTitleBG();
}
